package operator;

public class Operator1 {
	
	//arithmatic operator
	
	public static void main(String[] args) {
		int i=10;
		int j=20;
		
		//addition
		
		int k=i+j;
		System.out.println(k);
		
		
		//subtraction
		int l=i-j;
		System.out.println(l);
		
		//multiplication
		int m=i*j;
		System.out.println(m);
		
		//division
		int n=j/i;
		System.out.println(n);
		
		double s=i/j;
		System.out.println(s);
		
		//modulus operator
		int p=i%j;
		System.out.println(p);
		
	}

}
